using System;
using System.Collections.Generic;
using System.Text;

namespace PoolCube
{
    /// <summary>
    /// Specifies what mode the game is in.
    /// 
    /// Aim - the user is aiming a shot.
    /// Observe - the user is watching the result of a shot.
    /// Debug - allows the user to move freely within the pool cube, or choose one of the other two
    ///   camera modes, for debugging purposes.
    /// </summary>
    enum GameMode
    {
        Aim,
        Observe,
        Debug
    }
}
